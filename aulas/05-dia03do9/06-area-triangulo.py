# Área de um Triângulo: Solicite a base e a altura de um triângulo e calcule sua área usando a fórmula: 
# Area=base×altura/2

triangleBase = int(input("Insira a base do triangulo: "))
triangleHeight = int(input("Insira a altura do triangulo: "))

area = triangleBase * triangleHeight / 2

print(f"Área do triangulo: {area}")